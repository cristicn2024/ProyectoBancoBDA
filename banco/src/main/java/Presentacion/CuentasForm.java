    public void ConsultaCuentas(){
  DefaultTableModel modelo = (DefaultTableModel) Tabla.getModel();
    modelo.setRowCount(0);

    try {
        List<Cuenta> cuentas = clienteDAO.mostrarCuentas(idCliente);

        if (!cuentas.isEmpty()) {
            for (Cuenta cuenta : cuentas) {
                Object[] fila = {cuenta.getNoCuenta(), cuenta.getSaldo(), cuenta.getEstado(), cuenta.getFechaApertura(), cuenta.getIdCliente()};
                modelo.addRow(fila);
            }
        } else {
            JOptionPane.showMessageDialog(this, "No se encontraron cuentas para este cliente", "Cuentas no encontradas", JOptionPane.WARNING_MESSAGE);
        }
    } catch (PersistenciaException ex) {
        ex.printStackTrace();
        JOptionPane.showMessageDialog(this, "Error al consultar las cuentas: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    }
    }
